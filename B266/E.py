N = int(input())


ans = 3.5
count = 1

while count < N:
    if ans < 4:
        ans = ans*0.5 + 5*0.5
    elif ans < 5:
        ans = ans*(2/3) + 5.5*(1/3)
    elif ans < 6:
        ans = ans*(5/6) + 6*(1/6)
    count += 1
print(ans)

# 1,2,3,4,5,6 / 6 = 3.5
# (1,2,3,4,5,6), 4,5,6  to 3.5 * 0.5 + 15/3 * 0.5 = 1.75 + 2.5 = 4.25
# ((1,2,3,4,5,6), 4,5,6), 5,6 = 5.5*1/3 + 4.25*2/3 = 4.6666666666
